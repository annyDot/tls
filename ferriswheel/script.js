function dropdown() {
  var x = document.getElementById('dropdown-content');
  if (x.style.display === 'none') {
    x.style.display = 'block';
     document.getElementById("menu").classList.toggle("change");

  } else {
    x.style.display = 'none';
    document.getElementById("menu").classList.toggle("change");
  }
}
    

      
      
        
        
    
  
    

function menuFunction(x) {
  x.classList.toggle("change");
}

  
function dropDown() {
  var x = document.getElementById("dropdown-content");
  if (x.style.display === "none") {
    x.style.display = "block";
  } else {
    x.style.display = "none";
  }
}
    
    
  
 
      
      
        
        
    
  
    
